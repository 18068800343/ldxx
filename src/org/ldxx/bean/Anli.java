package org.ldxx.bean;

public class Anli {
	private String alId;
	private String alBt;
	private String alNr;
	private String alDate;
	private String alPicture;
	
	
	public String getAlId() {
		return alId;
	}
	public void setAlId(String alId) {
		this.alId = alId;
	}
	public String getAlBt() {
		return alBt;
	}
	public void setAlBt(String alBt) {
		this.alBt = alBt;
	}
	public String getAlNr() {
		return alNr;
	}
	public void setAlNr(String alNr) {
		this.alNr = alNr;
	}
	public String getAlDate() {
		return alDate;
	}
	public void setAlDate(String alDate) {
		this.alDate = alDate;
	}
	public String getAlPicture() {
		return alPicture;
	}
	public void setAlPicture(String alPicture) {
		this.alPicture = alPicture;
	}
	
	
	
}
